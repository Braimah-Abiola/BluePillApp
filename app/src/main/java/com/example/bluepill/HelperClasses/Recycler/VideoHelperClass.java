package com.example.bluepill.HelperClasses.Recycler;

public class VideoHelperClass {

    int image;
    String title;

    public VideoHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }


    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

}
